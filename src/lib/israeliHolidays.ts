/**
 * Israeli Ministry of Education school-year holidays (חופשות משרד החינוך).
 * Covers תשפ"ה (2024-2025), תשפ"ו (2025-2026), תשפ"ז (2026-2027).
 *
 * `end` is **exclusive** (FullCalendar convention) — the last visible day is end − 1 day.
 * Dates marked "הוא יום לימודים" in the official schedule are NOT included here.
 */

export interface HolidayEvent {
  id: string;
  title: string;
  start: string;
  end: string;
  display: "background";
  backgroundColor: string;
  classNames: string[];
  extendedProps: { isHoliday: true; holidayTitle: string };
}

const BG = "#fef9c3"; // soft yellow

function h(id: string, title: string, start: string, end: string): HolidayEvent {
  return {
    id,
    title,
    start,
    end,
    display: "background",
    backgroundColor: BG,
    classNames: ["holiday-bg"],
    extendedProps: { isHoliday: true, holidayTitle: title },
  };
}

